package ch03.lecture.p2inary;

public class C07Logical {
	public static void main(String[] args) {
		// 논리연산자
		// and : &&, &
		// or : ||, |
		// xor : ^
		// not : !
		// 피연산자 : boolean
		// 결과 : boolean
		
		/* and : true && true -> true
		 *       true && false -> false
		 *       false && true -> false
		 *       false && false -> false
		 */
		/*
		 * or : false || false -> false
		 *      false || true -> true
		 *      true || false -> true
		 *      true || true -> true
		 */
		/*
		 * xor : 다를때만 true
		 *       true ^ true -> false
		 *       true ^ false -> true
		 *       false ^ true -> true
		 *       false ^ false -> false
		 */
	}
}
